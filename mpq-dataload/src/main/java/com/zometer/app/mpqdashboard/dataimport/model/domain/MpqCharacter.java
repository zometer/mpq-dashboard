package com.zometer.app.mpqdashboard.dataimport.model.domain;

public class MpqCharacter {

	private Long mpqCharacterId ;
	private String name ;
	private String style ;

	public Long getMpqCharacterId() {
		return mpqCharacterId;
	}
	public void setMpqCharacterId(Long mpqCharacterId) {
		this.mpqCharacterId = mpqCharacterId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStyle() {
		return style;
	}
	public void setStyle(String style) {
		this.style = style;
	}

}
